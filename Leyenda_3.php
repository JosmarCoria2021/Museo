            <h2>KONA KAI MEXICALI</h2><br>
            El Kona Kai es un bar en Mexicali que durante el 2 de noviembre abre sus puertas y alimenta las leyendas del pueblo. 
Cuenta la leyenda que tres amigos el 1 de noviembre estaban poniéndose de acuerdo para salir en la noche después de haber quedado de verse al día siguiente para ir al panteón, sin embargo, sólo dos de ellos partieron a un bar, mientras que el otro se fue camino a su casa. Rumbo a su casa vio el recién abierto Kona Kai y decidió bajarse y conocerlo.
Al entrar, se sentó en la barra y el barman le dijo que estaban cerca de lo hora de cierre por lo que se apresuró a beber un trago, a lo que se acercó una señora a hacerle conversación y le invitó otro trago mientras platicaban. Entre los diálogos el hombre le compartió a la mujer que visitaría a su madre por el 2 de noviembre al panteón como todos los años y que ese año no sería la excepción. Con el bar apunto de cerrar el hombre se fue a su casa ya que debía estar temprano en el panteón como había acordado con sus amigos.
Al día siguiente, ya lo esperaban sus amigos y familiares en el cementerio, y al preguntarle el motivo de su tardanza y desvelo, éste les contestó que había ido al bar Kona Kai, y sus amigos se sorprendieron debido a su desconocimiento del lugar; para confirmar el hecho, éste les indicó la dirección, que es entre la Av. México y la López Mateos, ellos respondieron que ese lugar llevaba varios años cerrado.
Luego de la visita al panteón los tres amigos se dirigieron a la dirección donde estaba el bar, y el letrero era mucho más pequeño y más dañado del que uno de ellos recordaba. La puerta de entrada estaba abierta a lo que el hombre decidió entrar, en el lugar sólo quedaban ruinas, quedó desconcertado, estaban los vasos que había ocupado así como su dinero; ahí recordó lo que siempre le dijo su madre: “el 2 de noviembre los muertos visitan a los vivos”. 
Se cuenta en Mexicali que cada día de muertos el bar abre sus puertas
