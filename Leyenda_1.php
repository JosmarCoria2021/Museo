Con el pasar de los años, una familia se encontraba de viaje para celebrar con otros parientes una de las regiones aledañas. 
<br>
 El festejo se extendió y era muy tarde para regresar a casa, por lo que la mujer insistió en quedarse 
 y regresar al día siguiente, pero su esposo prefirió devolverse a Aguascalientes en ese momento.
Al pasar de nuevo por la carretera, la pareja vio a un par de niños jugando a uno de los costados de la misma, 
y pese a que era extraño por la hora y el lugar, estaban tan cansados que prefirieron no hacer caso a lo que vieron y seguir su camino. 
Sin embargo, unos metros más adelante, empezaron a percibir un olor fétido y sintieron asco.
El hombre detuvo el auto y al mirar hacia atrás, tanto él como su esposa se dieron cuenta de que los mismos 
niños que habían visto poco antes estaban parados justo detrás del vehículo con los rostros totalmente destruidos y 
