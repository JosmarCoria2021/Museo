            <h2> ¡YO NO CONFIESO A LOS MUERTOS!  MICHOACÁN</h2><br>
            En las cercanías de la Iglesia de San Francisco en Morelia, Michoacán, había una vivienda en la cual 
            asustaban, la cual estaba ubicada en un callejón. Un hombre que vendía paños, sedas y mantones, 
            tras mucho viajar por las ciudades de la Nueva España, resolvió establecerse y residir en Valladolid, con 
            la finalidad de casarse con una hermosa y rica joven, para después retornar a su natal Santander, España. 
            En su tienda vino a conocer a doña Inés de la Cuenca y Fragua, una linda y generosa huérfana y heredera de una de las fincas de mayor riqueza de Tierra Caliente. 
            Seducido por sus perfectas cualidades, don Diego Pérez de Estrada consiguió enamorarla. Inés lo adoraba con sinceridad, mas no así Diego, él estaba motivado por el interés más egoísta.
A Don Diego le encantaba la parranda y era muy mujeriego, se trajeaba elegantemente y exhibía costosas gemas. 
En confianza era de muy mal hablar, pero habitualmente mostraba una imagen muy distinta ante las personas que no eran de su grupo de amigos.
Cierto día, don Diego solicitó a la joven su mano. Previo a responderle, Inés fue con su confesor Fray Pedro de 
la Cuesta, con el propósito de consultarle si era conveniente o no casarse. Fray Pedro, que era un hombre muy justo 
y bondadoso, resolvió informarse de la categoría de sujeto que era el tal Diego Pérez.
Llegó a enterarse de que era parte de una honorable familia de Santander, pero que era la oveja negra del grupo 
familiar y que había arribado a la Nueva España trayendo parte de la herencia que le legaron. Al agotarse su herencia 
por motivo del derroche de Diego en sus continuas fiestas, se dedicó a comerciar telas y mantones de Manila, hasta que 
pudo llegar a Valladolid.
Fray Pedro conoció de la mala fama de don Diego y de que sumado a ello se ufanaba de que jamás sentía amor por mujer
 alguna motivado a haber vivido una existencia tan disipada. El fraile sugirió a la bella Inés que no se casará, y la niña le hizo caso y repudió al presunto enamorado. Al saberse rechazado, airado y desalmado, prometió vengarse de fray Pedro. Para ello puso en venta su tienda y se fue a residir a un habitación ubicada en una callejuela por el costado norte del cementerio de San Francisco, en unión con un empleado suyo.
En una noche en que una horrible tempestad descendía sobre la ciudad, un hombre cubierto se aproximó hasta el 
portón del convento el cual golpeó y le fue abierto por un encapuchado portero. El primer hombre le habló al segundo con estas palabras: -¡Hermano portero, en un lugar próximo un pobre hombre agonizante desea recibir la confesión de fray Pedro de la Cuesta!
Fray Pedro y el hombre cubierto marcharon hasta el cuartucho iluminado por una débil vela, el sacerdote se aproximó 
al lecho de muerte, pero al hablarle al presunto moribundo, que era el mismísimo don Diego, éste no respondía.
El padre, angustiado, le gritaba, y cuando le quitó el manto que le cubría lo consiguió muerto de una puñalada que 
se le dio con la misma daga con la que tenía pensado asesinar al padre fray. Al contemplarlo, fray Diego se distanció 
del fallecido a la vez que gritaba: -¡Yo le doy la confesión a los vivos, pero jamás a los muertos! Y se fue corriendo.
 Al próximo día el suceso era del dominio público de toda Valladolid, y a partir de ese instante a la callejuela se 
 le dio el nombre de El Callejón del Muerto, y quedo registrada como una de las leyendas de Michoacán.
