            <h2>LA MUJER QUE BAILÓ CON EL DIABLO DE TIJUANA</h2>
            <br>
            La avenida Revolución tenía un toque tétrico y el olor a azufre que despedía 
            del interior del Bar Aloha testificaba que algo no andaba bien.
Era un jueves, algunas personas señalan que era uno de esos días santos en los que hay 
poca clientela y pocos turistas deambulando por las calles, los socorristas de la Cruz Roja recibieron el 
reporte de una fuerte descarga eléctrica que se había suscitado en el interior del establecimiento y que había heridos, 
nunca pensaron que ese servicio se les quedaría grabado para siempre. Uno de ellos mencionó que a la hora en que recibieron el reporte el cielo estaba más oscuro que de costumbre, otro prefería no acordarse porque la sola imagen le ocasionaba escalofríos.
No les fue muy fácil precisar lo que pasó, sólo coincidieron en que al llegar al bar el ambiente era diferente; se
percibía un olor muy peculiar y la impresión se dibujaba con una claridad espeluznante en el rostro de los testigos 
que se resistían a creer lo que había sucedido, en el interior de la pista de baile estaba el cuerpo de una mujer 
en medio de un círculo de cenizas completamente calcinado, los testigos dijeron que estaba bailando con un joven que 
cuando ocurrió todo prefirió correr hacia los baños, policías y socorristas de inmediato se trasladaron a verificar este 
testimonio, pero en el interior de los sanitarios no había nadie y se veían pocas probabilidades de escapar, de hecho difícilmente alguien podría haberlo conseguido. Al ver el cadáver, los paramédicos no podían asegurar que se trataba de una descarga eléctrica, el grado de calcinación era increíble.
Hace poco más de 20 años que esta historia circula por las calles de Tijuana y todos la conocen como la mujer que
 bailó con el Diablo esa noche, una de las versiones señala que la víctima era una jovencita, hija de familia que 
 vivía bajo el yugo de su madre y no salía mucho a la calle.
Esa tarde de jueves, la mujer se decidió a salir a la calle a un bar para bailar con algún muchacho 
porque estaba harta de su situación familiar “Hoy voy a bailar aunque sea con el Diablo”, fueron las palabras que le
dijo a una amiga cuando se escapó de su casa, entonces decidió entrar en el Aloha, no había mucha gente, unos cuantos parroquianos solamente. 
Un hombre de vestir elegante se percató de la presencia de la mujer que esperaba con ansias poder divertirse y se le acercó, quienes lo vieron coinciden en que el muchacho no estaba de mal ver y traía un traje bastante fino. 
La mujer no pudo resistirse y accedió a la invitación, los minutos siguientes y los últimos de su vida, quizás también fueron los más extraños.
Las mentes más despiertas y creativas alegan que la pareja estaba literalmente flotando sobre la pista y q
ue al hombre se le veía una pata de cabra y la otra de gallo, entonces se dio lo impensable, 
la famosa descarga eléctrica que salió justo del centro de la pista donde bailaba la pareja. 
El cuerpo de la mujer desfalleció enseguida, el hombre misterioso corrió a toda prisa hacia los baños, donde 
finalmente desapareció. El final no fue difícil adivinarlo, todos terminaron señalando al Diablo como el sospechoso 
sujeto que se esfumó en los baños. Y es que esa noche aprovechando la situación salió para hacerle la última jugarreta de su vida a una mujer que falleció en sus brazos.
